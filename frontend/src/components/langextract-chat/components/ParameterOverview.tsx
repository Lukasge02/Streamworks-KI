// Schema v2.0 PROTOTYP: Get parameters based on job type detection
const getJobParameterDefinitions = (jobType: string | null): Record<string, ParameterDefinition> => {
  // Always show auto-generated parameters
  let definitions: Record<string, ParameterDefinition> = { ...AUTO_GENERATED_PARAMETER_DEFINITIONS }

  // Add job-specific parameters if job type is detected
  if (jobType) {
    const normalizedJobType = jobType === 'FileTransfer' ? 'FILE_TRANSFER' : jobType

    if (JOB_SPECIFIC_PARAMETER_DEFINITIONS[normalizedJobType]) {
      definitions = {
        ...definitions,
        ...JOB_SPECIFIC_PARAMETER_DEFINITIONS[normalizedJobType]
      }
    }
  }

  return definitions
}

