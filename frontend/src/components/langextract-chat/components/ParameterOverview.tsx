/**
 * üßπ PHASE 3 FIX: Filter out LangExtract artifacts from UI parameters
 */
function filterParameterArtifacts(parameters: Record<string, any>): Record<string, any> {
  const filtered: Record<string, any> = {}

  // Technical fields that should never be displayed in UI
  const technicalFields = new Set([
    'extractions',
    'text',
    '_document_id',
    'extraction_class',
    'extraction_text',
    'confidence',
    'highlighted_ranges',
    'source_grounding',
    'metadata',
    '__dict__',
    '__class__',
    'api_calls_made',
    'extraction_duration',
    'warnings',
    'extraction_errors',
    'processing_time'
  ])

  for (const [key, value] of Object.entries(parameters)) {
    // Skip technical fields
    if (technicalFields.has(key.toLowerCase())) {
      continue
    }

    // Skip empty, null, or undefined values
    if (value === null || value === undefined || value === '') {
      continue
    }

    // Skip object references and technical artifacts
    if (typeof value === 'string') {
      if (value.toLowerCase().includes('[object') ||
          value.toLowerCase().includes('undefined') ||
          value.toLowerCase() === 'null') {
        continue
      }
    }

    // Only include clean, meaningful parameters
    filtered[key] = value
  }

  return filtered
}

/**
 * Format parameter values for display, handling edge cases
 */
function formatParameterValue(value: any): string {
  if (value === null || value === undefined) {
    return 'null'
  }

  if (typeof value === 'string') {
    // Truncate very long values
    if (value.length > 50) {
      return value.substring(0, 47) + '...'
    }
    return value
  }

  if (typeof value === 'boolean') {
    return value ? 'true' : 'false'
  }

  if (typeof value === 'number') {
    return value.toString()
  }

  // For objects, arrays, etc.
  try {
    const str = JSON.stringify(value)
    if (str.length > 50) {
      return str.substring(0, 47) + '...'
    }
    return str
  } catch {
    return String(value)
  }
}